.MODEL SMALL
.STACK 64

.DATA
TAB DW 10, 20, 30, 55, 60, 175, 88, 250, 99, 120
; TAB DW 10 DUP(20)
SUM DW 0

.CODE
MAIN PROC FAR
    MOV AX, @DATA
    MOV DS, AX

    ; CALL INP_DEC
    MOV BX, 00H
    MOV CX, 000AH
    SUM_LOOP:
        MOV AX, TAB[BX]
        CMP AX, 30D
        JB SKIP
        CMP AX, 100D
        JA SKIP
        ADD SUM, AX
        SKIP:
        ADD BX, 2
    LOOP SUM_LOOP

    MOV AX, SUM
    CALL DISP_HEX

    MOV AX, 4C00H
    INT 21H
MAIN ENDP

DISP_HEX PROC
XOR CX,CX
OL:
XOR DX,DX
L1:
CMP AX,  10H
JB LESS
SUB AX, 10H
INC DX
JMP L1

LESS:
PUSH AX
MOV AX, DX
INC CX
CMP AX, 00H
JNE OL

L2:
    POP DX
    CMP DX, 09D
    JBE DIGITS
    ADD DX, 07H
    DIGITS:
    ADD DX, 30H
    MOV AH, 02H
    INT 21H
    LOOP L2

RET
DISP_HEX ENDP

END MAIN