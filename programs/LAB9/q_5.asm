.MODEL SMALL
.STACK 100H
.DATA
	MSG DB "Enter a string:$"
	STRING DB 80, ?, 80 DUP ('$')
	NEWLINE DB 13, 10, '$'
.CODE
START:
	MOV AX, @DATA
	MOV DS, AX

	;START WRITING FROM HERE
	MOV AH, 00H
	MOV AL, 03H
	INT 10H

	MOV AH, 09H
	MOV DX, OFFSET MSG
	INT 21H

	LEA DX, STRING
	MOV AH, 0AH
	INT 21H

	MOV SI, OFFSET STRING+2
	MOV CL, [STRING+1] ;LOADING THE 2ND PARAM THAT CONTAINS STRLEN
	MOV CH, 0
	
	MOV DH, 2
	MOV DL, 5

PRINT_LOOP:
	CMP CL, 0
	JE END_PROGRAM

	MOV AL, [SI]
	CMP AL, ' '
	JE NEXT_LINE

	MOV AH, 02H
	MOV BH, 0
	INT 10H

	MOV AH, 0EH
	INT 10H

	INC SI
	DEC CL
	INC DL
	JMP PRINT_LOOP

NEXT_LINE:
	INC DH
	ADD DL, 2
	INC SI
	DEC CL
	JMP PRINT_LOOP

END_PROGRAM:
	MOV AH, 00H
	INT 16H

	MOV AH, 4CH
	INT 21H

END START
