.MODEL SMALL
.STACK 100H
.DATA
	MSG DB "Enter a string:$"
	STRING DB 24, 0, 24 DUP ('$')
.CODE
START:
	MOV AX, @DATA
	MOV DS, AX

	MOV AH, 00H ;CLEAR SCREEN
	MOV AL, 03H
	INT 10H

	MOV AH, 09H ;DISPLAY MSG PROMPT
	LEA DX, MSG
	INT 21H

	LEA DX, STRING
	MOV AH, 0AH ;READING THE USER INPUT
	INT 21H

	LEA SI, STRING+1
	MOV AL, [SI]
	MOV CL, AL
	LEA SI, STRING + 2

	MOV DH, 1
	MOV DL, 40
		
PRINT_LOOP:
	CMP CL, 0
	JE END_INPUT

	MOV AH, 02H
	MOV BH, 0
	INT 10H

	MOV AH, 0EH
	MOV AL, [SI]
	INT 10H

	INC SI
	DEC CL
	INC DH
	ADD DL,2
	JMP PRINT_LOOP

END_INPUT:
	MOV AH, 4CH
	INT 21H
END START
