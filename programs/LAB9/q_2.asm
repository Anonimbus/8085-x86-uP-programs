.MODEL SMALL
.STACK 100H
.DATA
	MSG DB "Enter a string:$"
	STRING DB 60, 0, 60 DUP ('$')
.CODE
START:
	MOV AX, @DATA
	MOV DS, AX

	MOV AH, 00H ;CLEAR SCREEN
	MOV AL, 03H
	INT 10H

	MOV AH, 09H ;DISPLAY MSG PROMPT
	LEA DX, MSG
	INT 21H

	LEA DX, STRING
	MOV AH, 0AH ;READ STRING
	INT 21H

	MOV AX, 0003H ;CLEAR SCREEN
	INT 10H
	
	LEA SI, STRING+2
	MOV AL, [SI]

;SHIFT THE CURSOR POSITION TO CENTER

	MOV AH,02H
	MOV BH, 00
	MOV DL, 40 ;COLUMN
	MOV DH, 12 ;ROW
	INT 10H

	LEA DX, STRING+2
	MOV AH, 09H
	INT 21H

	MOV AH, 4CH
	INT 21H
END START
